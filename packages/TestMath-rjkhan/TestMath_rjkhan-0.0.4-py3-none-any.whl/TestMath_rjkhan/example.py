def add(number1, number2):
    """
    return sum of integer

    :param number1: should be integer number
    :param number2: should be integer number
    :type number1: Integer
    :type number2: Integer
    :return: addition of integer
    :rtype: int

    """
    return number1 + number2


def sub(number1, number2):
    """
       return sum of integer

       :param number1: should be integer number
       :param number2: should be integer number
       :type number1: Integer
       :type number2: Integer
       :return: subtraction of integer
       :rtype: int

       """
    return number1 - number2


def mul(number1, number2):
    """
       return sum of integer

       :param number1: should be integer number
       :param number2: should be integer number
       :type number1: Integer
       :type number2: Integer
       :return: multiply of integer
       :rtype: int

       """
    return number1 * number2



def div(number1, number2):
    """
    return sum of integer

    :param number1: should be integer number
    :param number2: should be integer number
    :type number1: Integer
    :type number2: Integer
    :return: divide of integer
    :rtype: int
    """
    return number1 / number2



